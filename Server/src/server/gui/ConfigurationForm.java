    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        Validate validate = new Validate();
        String dbms = addDBMS.getText().trim();
        if (!validate.dataBaseManagementSystem(dbms)) {
            JOptionPane.showMessageDialog(this, "Ne moze to ime za SUBP");
            return;
        }
        String url = addURL.getText().trim();
        String driver = addDriver.getText().trim();
        String user = addUser.getText().trim();
        String password = addPassword.getText().trim();

        DataBaseUtils.getInstance().setURL(dbms, url);
        DataBaseUtils.getInstance().setDriver(dbms, driver);
        DataBaseUtils.getInstance().setUser(dbms, user);
        DataBaseUtils.getInstance().setPassword(dbms, password);
        ((ConfigurationTableModel) jTable.getModel()).fireTableDataChanged();
    }//GEN-LAST:event_addButtonActionPerformed

