    private void fixForm() {
        for (String dbms : DataBaseUtils.getInstance().getDBManagementSystems()) {
            combo.addItem(dbms);
        }
        combo.setSelectedItem(DataBaseUtils.getInstance().getCurrentDBMS());
        jTable.setModel(new ConfigurationTableModel());

        addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosing(java.awt.event.WindowEvent windowEvent) {
                DataBaseUtils.getInstance().saveProperies();
            }
        });
    }
